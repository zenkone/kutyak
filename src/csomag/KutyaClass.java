
package csomag;


public class KutyaClass {
    private String fajta;
    private String nem;
    private String kor;
    private String nev;

    public KutyaClass(String fajta, String nem, String kor, String nev) {
        this.fajta = fajta;
        this.nem = nem;
        this.kor = kor;
        this.nev = nev;
    }

    public String getFajta() {
        return fajta;
    }

    public String getNem() {
        return nem;
    }

    public String getKor() {
        return kor;
    }

    public String getNev() {
        return nev;
    }
    
    
    
    
    
}
